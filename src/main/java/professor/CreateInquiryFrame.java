    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    // 문의내용 입력 확인
    String message = jTextArea1.getText().trim();
    if (message.isEmpty()) {
        JOptionPane.showMessageDialog(this, "문의 내용을 입력하세요.", "경고", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // 중요도 선택
    boolean isPriority = jRadioButton2.isSelected(); // "긴급"이 선택되었으면 true

    // Inquiry 객체 생성 (답변/처리여부는 기본값)
    Inquiry inquiry = new Inquiry(
        userName,
        userId,
        message,
        java.time.LocalDateTime.now(),
        false,      // isChecked: 미처리
        "",         // answeredInquiries: 답변 없음
        isPriority  // 중요도
    );

    // InquiryExcel에 저장
    InquiryExcel inquiryExcel = new InquiryExcel();
    boolean success = inquiryExcel.addInquiry(inquiry);

    if (success) {
        JOptionPane.showMessageDialog(this, "문의가 등록되었습니다.", "알림", JOptionPane.INFORMATION_MESSAGE);
    } else {
        JOptionPane.showMessageDialog(this, "문의 등록에 실패했습니다.", "오류", JOptionPane.ERROR_MESSAGE);
      }
    }//GEN-LAST:event_jButton2ActionPerformed

